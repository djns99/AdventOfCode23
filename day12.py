import functools

g_runs = []


def validate(condition, validate_state):
    fail_tuple = False, False, False, None
    #print(condition, g_runs, validate_state)
    r_start, c_start, _ = validate_state
    c_idx = c_start
    for r_idx, r in enumerate(g_runs[r_start:]):
        r_idx += r_start
        #print(r_idx, r, c_idx, condition[c_idx])
        base_c_idx = c_idx

        # Find the next run
        while condition[c_idx] == '.':
            c_idx += 1

        count = 0
        # Count the next run
        while condition[c_idx] == '#':
            c_idx += 1
            count += 1
            if count > r:
                #print("count > r @{},{}".format(r_idx, c_idx))
                return fail_tuple
            elif c_idx == len(condition):
                return r == count, False, False, None

        if condition[c_idx] == '?':
            num_left_in_run = r - count
            can_do_blank = count == 0 or count == r
            return True, num_left_in_run > 0, can_do_blank, (r_idx, base_c_idx, count)

        if count != r:
            #print("count != r")
            return fail_tuple

    return True, False, False, None


iters = 0

condition = ""
positions = []
@functools.lru_cache(maxsize=None)
def solve1_r(full_str, cur_run, curr_position, num_to_fix, vstate = (0, 0, 0)):
    #print(cur_run, curr_position, num_to_fix, vstate)

    global g_runs
    global iters
    global condition
    global positions

    iters += 1
    if iters % 1000000 == 0:
        print(condition, "after", iters)

    vres, numleftrun, canblank, newvstate = validate(condition, vstate)

    if not vres:
        #print(condition, "was invalid")
        return 0
    #print(condition, "was valid")
    if num_to_fix == 0 or num_to_fix == len(positions) - curr_position:
        sub = '.' if num_to_fix == 0 else '#'
        for p in positions[curr_position:]:
            condition[p] = sub
        val, _, _, _ = validate(condition, vstate)
        #print("Final", condition, "was", "valid" if val else "invalid")
        for p in positions[curr_position:]:
            condition[p] = '?'
        return 1 if val else 0

    #print(condition, numleftrun, num_to_fix, positions)
    #assert num_to_fix <= len(positions)
    #assert numleftrun <= num_to_fix


    pos = positions[curr_position]
    c = 0
    if canblank:
        condition[pos] = '.'
        c += solve1_r(full_str, 0, curr_position+1, num_to_fix, newvstate)


    if numleftrun > 0:
        condition[pos] = '#'
        c += solve1_r(full_str, cur_run + 1, curr_position+1, num_to_fix - 1, newvstate)

    condition[pos] = '?'

    return c

def solve1(incond, runs):
    global g_runs
    global iters
    global condition
    global positions

    condition = list(incond)

    iters = 0
    num_damaged = sum(runs)
    count_damaged = sum([1 for x in condition if x == '#'])
    positions = [i for i, x in enumerate(condition) if x == '?']
    num_to_fix = num_damaged - count_damaged
    g_runs = runs

    solve1_r.cache_clear()
    return solve1_r(incond, 0, 0, num_to_fix)


def part1(lines):
    result = 0
    for l in lines:
        l = l.split(' ')
        conditions = l[0]

        runs = list(map(int, l[1].split(',')))
        count = solve1(conditions, runs)

        print(conditions, "with runs", runs, "had", count, "solutions")
        result += count

    print(result)


def part2(lines):
    result = 0
    for l in lines:
        l = l.split(' ')
        conditions = ((l[0] + "?") * 5)[:-1]

        runs = list(map(int, l[1].split(',') * 5))
        print("Testing", l[0], conditions, runs)
        count = solve1(conditions, runs)

        print(conditions, "with runs", runs, "had", count, "solutions")
        result += count

    print(result)


